#include <stdio.h>

void main()
{
	int x,y,z;
	
	//taking inputs
	printf("please enter number 1: ");
	scanf("%d",&x);
	
	printf("please enter number 2: ");
	scanf("%d",&y);
	
	printf("please enter number 3: ");
	scanf("%d",&z);
	
	//printing entered values in reverse
	printf("number 3: %d",z);
	printf("\nnumber 2: %d",y);
	printf("\nnumber 1: %d",x);
	
}